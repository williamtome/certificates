     * @return \Illuminate\Http\Response
     */
    public function store(TemplateRequest $request)
    {
        Template::create([
            'title' => $request->title,
            'front'=> $request->front,
            'back'=> $request->back
        ]);
        return redirect()->route('template.index');
    }

